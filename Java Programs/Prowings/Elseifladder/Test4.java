package Elseifladder;

public class Test4 {
	public static void main(String[] args) {
		int a=40;
		if(a==10)
		{
			System.out.println("Riddhi");
		}
		else if(a==20)
		{
			System.out.println("Shraddha");
		}
		else if(a==30)
		{
			System.out.println("Sejal");
		}
		else
		{
			System.out.println("Default condition");
		}
	}

}
