package Elseifladder;

public class Test2 {
	
	public static void main(String[] args) {
		int a=20;
		if(a==10)
		{
			System.out.println("Ten");
		}
		else if(a==20)
		{
			System.out.println("Twenty");
		}
		else if(a==30)
		{
			System.out.println("Thirty");
		}
		else
		{
			System.out.println("Default condition");
		}
	}

}
