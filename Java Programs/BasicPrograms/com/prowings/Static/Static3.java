package com.prowings.Static;

public class Static3{

	 public static void main(String[] args) {
		System.out.println("Hello\t World");   // (\t) Insert a tab in the text at this point
		System.out.println("Hello\b Sai");     // (\b) Insert a backspace in the text at this point
		System.out.println("Hello\n Parth");   // (\n) Insert a newline in the text at this point
		System.out.println("hi Hello\r World  Hi Sai"); // (\r) Insert a carriage return in the text at this point
		System.out.println("Hello \f World \f Hi Parth");// (\f) Insert a form feed in the text at this point
		System.out.println("Hello World \'Sai\'"); // (\ ') Insert a single quote character in the text at this point
		System.out.println("Hello World \"Parth\"");// (\") Insert a double quote character in the text at this point
		System.out.println("Hello\\World"); // (\\)Insert a blackslash character in the text at this point
	}
}
